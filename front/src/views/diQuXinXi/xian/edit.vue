<template>
  <div>
    <Card>
      <div slot="title">
        <div class="edit-head">
          <a @click="close" class="back-title">
            <Icon type="ios-arrow-back" />返回
          </a>
          <div class="head-name">编辑</div>
          <span></span>
          <a @click="close" class="window-close">
            <Icon type="ios-close" size="31" class="ivu-icon-ios-close" />
          </a>
        </div>
      </div>
      <Form ref="form" :model="form" :label-width="100" :rules="formValidate" label-position="left" >        <FormItem label="编码" prop="number"  >
          <Input v-model="form.number" clearable style="width:570px"/>
        </FormItem>
        <FormItem label="城市" prop="city"  >
          <al-cascader :placeholder="cityTemp" @on-change="changeCity" style="width: 500px" />
          <!-- <Input v-model="form.city" clearable style="width:570px"/> -->
        </FormItem>
        <!-- <FormItem label="县区" prop="title"  >
          <Input v-model="form.title" clearable style="width:570px"/>
        </FormItem> -->
        <FormItem label="县级类型" prop="countyType"  >
            <Select :placeholder="form.countyType" clearable style="width: 200px" @on-change="changeCountyType">
              <Option
                v-for="(item) in this.countyTypeList"
                :key="item.title"
                :value="item"
              >{{item.title}}</Option>
            </Select>
            <!-- <Input v-model="form.countyType" clearable style="width:570px"/> -->
          </FormItem>
        <FormItem label="店面密度" prop="miDu"  >
          <Input v-model="form.miDu" clearable style="width:570px"/>
        </FormItem>
        <FormItem label="单店销量" prop="sellNumber"  >
          <Input v-model="form.sellNumber" clearable style="width:570px"/>
        </FormItem>
        <FormItem label="是否封存" prop="ban"  >
          <i-switch v-model="form.ban" true-value="封存" false-value="正常" ></i-switch>
        </FormItem>
        <FormItem label="备注" prop="remark"  >
          <Input v-model="form.remark" clearable style="width:570px"/>
        </FormItem>
        <Form-item class="br">
          <Button
            @click="handleSubmit"
            :loading="submitLoading"
            type="primary"
          >提交并保存</Button>
          <Button @click="handleReset">重置</Button>
          <Button type="dashed" @click="close">关闭</Button>
        </Form-item>
      </Form>
    </Card>
  </div>
</template>

<script>
// 根据你的实际请求api.js位置路径修改
import { editXian,getAllCountyType } from "./api.js";
export default {
  name: "edit",
  components: {
    },
  props: {
    data: Object
  },
  data() {
    return {
      cityTemp:'',
      submitLoading: false, // 表单提交状态
      form: { // 添加或编辑表单对象初始化数据
      number: "",
      city: "",
      title: "",
      countyType: "",
      miDu: "",
      sellNumber: "",
      ban: true,
      remark: "",
      sheng:"",
    },
    // 表单验证规则
    formValidate: {
      number: [{ required: true, message: "不能为空", trigger: "blur" }],
    },
      countyTypeList:[],
    };
  },
  methods: {
    init() {
      this.handleReset();
      this.form = this.data;
      this.cityTemp = this.form.city + '/' + this.form.title;
      this.getAllCountyTypeFx();
    },
    changeCountyType(e){
      this.form.countyType = e.title;
    },
    getAllCountyTypeFx(){
      var that =this;
      getAllCountyType().then(res => {
        that.countyTypeList = res.result;
      })
    },
    changeCity(e){
      // console.log(e);
      this.form.sheng = e[0].name;
      this.form.city = e[1].name;
      this.form.title = e[2].name;
    },
    handleReset() {
      this.$refs.form.resetFields();
    },
    handleSubmit() {
      this.$refs.form.validate(valid => {
        if (valid) {
          editXian(this.form).then(res => {
            this.submitLoading = false;
            if (res.success) {
              this.$Message.success("操作成功");
              this.submited();
            }
          });
        }
      });
    },
    close() {
      this.$emit("close", true);
    },
    submited() {
      this.$emit("submited", true);
    }
  },
  mounted() {
    this.init();
  }
};
</script>
